USE shop;

DROP TABLE IF EXISTS logs;

CREATE TABLE logs (
	created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
	title_table VARCHAR(255),
	id INT NOT NULL,
	name VARCHAR(255)
) ENGINE=Archive; -- указываем тип


DROP PROCEDURE IF EXISTS insert_into_logs; 
DELIMITER //
CREATE PROCEDURE insert_into_logs (IN -- создаем процедуру добавления данных в таблицу
	title_table VARCHAR(255),
	id INT,
	name VARCHAR(255))
BEGIN
	
	 INSERT INTO logs (title_table, id, name)
	 VALUES (title_table, id, name);
	
END//
DELIMITER ;


DROP TRIGGER IF EXISTS shop.insert_into_logs_from_u ; -- создаем триггеры, которые автоматически будут заполнять таблицу
DELIMITER //
CREATE TRIGGER insert_into_logs_from_u
AFTER INSERT ON users 
FOR EACH ROW 
BEGIN 
	CALL insert_into_logs('users', NEW.id, NEW.name);
END //
DELIMITER ;


DROP TRIGGER IF EXISTS insert_into_logs_from_c ;
DELIMITER //
CREATE TRIGGER insert_into_logs_from_c
AFTER INSERT ON catalogs
FOR EACH ROW 
BEGIN 
	CALL insert_into_logs('catalogs', NEW.id, NEW.name);
END //
DELIMITER ;


DROP TRIGGER IF EXISTS insert_into_logs_from_p ;
DELIMITER //
CREATE TRIGGER insert_into_logs_from_p
AFTER INSERT ON products
FOR EACH ROW 
BEGIN 
	CALL insert_into_logs('products', NEW.id, NEW.name);
END //
DELIMITER ;


